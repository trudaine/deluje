  public static int doFMNew(int carrierPhase, int phaseShift) {
    int phaseSmall = (carrierPhase >>> 8) + phaseShift;
    int strength2 = phaseSmall & 65535;
    int readOffset = ((phaseSmall >>> (24 - 8 - 1)) & 0b0111111110);

    int s = sineWaveDiff[readOffset] & 0xFFFF;
    int d = sineWaveDiff[readOffset + 1] & 0xFFFF;

    int value = (short) s << 16;
    int diff = (short) d;

    return value + diff * strength2;
  }

  public static void render(int[] phaseVector, int[] output) {
    for (int i = 0; i < phaseVector.length; i++) {
      int phase = phaseVector[i];

      int strength2 = (phase >>> (32 - 16 - kSineTableSizeMagnitude + 1)) & 0x7FFF;
      int index = ((phase >>> (32 - kSineTableSizeMagnitude)) << 1);

      int s = sineWaveDiff[index] & 0xFFFF;
      int d = sineWaveDiff[index + 1] & 0xFFFF;

      int sine = (short) s << 16;
      int diff = (short) d;

      output[i] = sine + diff * strength2 * 2;
    }
  }
